# Escribir en pantalla los números enteros entre dos
# números enteros que se piden al usuario

inicio = int(input("Escribe el número inicial: "))
fin    = int(input("Escribe el número final: "))

while inicio <= fin:
    print(inicio)
    inicio += 1

