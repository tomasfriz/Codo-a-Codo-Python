#Ingresar una edad y determinar si es mayor de edad o no.

edad = int(input("Ingrese su edad: "))
if edad >= 18:
    print("Es mayor de edad")   # Si la edad es mayor o igual a 18, imprime "Es mayor de edad"
else:
    print("Es menor de edad")   # Si la edad es menor a 18, imprime "Es menor de edad"  
# Fin del programa